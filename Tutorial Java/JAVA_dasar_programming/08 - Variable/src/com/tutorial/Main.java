package com.tutorial;

public class Main {

    public static void main(String[] args){
        // kita akan membuat variabel
        /* Tipe data */
        int a = 10; // assignment => cara menaruh nilai dalam sebuah variabel
        System.out.println("Nilai x = " + a);

        a = 20;
        System.out.println("Nilai x baru = " + a);

        // kita akan membuat sebuah deklarasi
        int b; // deklarasi => dapat menaruh nilai setelah di deklarasi

        b = 7;
        System.out.println("Nilai b = " + b);
    }
}
