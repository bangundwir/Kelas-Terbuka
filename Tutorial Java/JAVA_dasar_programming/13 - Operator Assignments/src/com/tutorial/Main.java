package com.tutorial;

public class Main {

    public static void main(String[] args){

        // Operator assignment

        // Assignment operator jumlah
        int a = 1;
        a += 10; //a = a + 10;
        System.out.println("Nilai a = " + a);

        // Assignment operator Pengurangan
        int b = 100;;
        b -= 25;
        System.out.println("Nilai b = " + b);

        // Assignment operator Perkalian
        int c = 100;;
        c *= 20;
        System.out.println("Nilai b = " + c);

        // Assignment operator Pembagian
        int d = 100;;
        d /= 20;
        System.out.println("Nilai b = " + d);

        // Assignment operator Modulus
        int e = 10;;
        e %= 7;
        System.out.println("Nilai b = " + e);
    }
}
